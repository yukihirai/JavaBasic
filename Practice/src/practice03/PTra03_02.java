public class PTra03_02 {
	public static void main(String[] args) {
		// ★ int型の変数numを宣言し、65で初期化してください
        int num=65;

		// ★ 変数numの値が65以上の場合は「合格です」と出力し、それ以外であれば「不合格です」を出力してください
		// ※プログラムは何行書いても良いです
           if(num>=65) {
        	   System.out.println("合格です");
        	
           }else {
        	   System.out.println("不合格です");
           }

	}
}
